package finalhorse.vo;

public class HorseRecord {
	private String rdate;
	private String owner;
	private float avg_rank;
	private int tt_win1;
	private int tt_win2;
	private int tt_prize;
	private float h_weight;
	private int round;
	private String name;
	private String origin;
	private String sex;
	private int age;
	private float p_weight;
	private String player;
	private String teacher;

	HorseRecord(){}
	
	public String getRdate() {
		return rdate;
	}

	public void setRdate(String rdate) {
		this.rdate = rdate;
	}

	public String getOwner() {
		return owner;
	}

	public void setOwner(String owner) {
		this.owner = owner;
	}

	public float getAvg_rank() {
		return avg_rank;
	}

	public void setAvg_rank(float avg_rank) {
		this.avg_rank = avg_rank;
	}

	public int getTt_win1() {
		return tt_win1;
	}

	public void setTt_win1(int tt_win1) {
		this.tt_win1 = tt_win1;
	}

	public int getTt_win2() {
		return tt_win2;
	}

	public void setTt_win2(int tt_win2) {
		this.tt_win2 = tt_win2;
	}

	public int getTt_prize() {
		return tt_prize;
	}

	public void setTt_prize(int tt_prize) {
		this.tt_prize = tt_prize;
	}

	public float getH_weight() {
		return h_weight;
	}

	public void setH_weight(float h_weight) {
		this.h_weight = h_weight;
	}

	public int getRound() {
		return round;
	}

	public void setRound(int round) {
		this.round = round;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getOrigin() {
		return origin;
	}

	public void setOrigin(String origin) {
		this.origin = origin;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public int getAge() {
		return age;
	}

	public void setAge(int age) {
		this.age = age;
	}

	public float getP_weight() {
		return p_weight;
	}

	public void setP_weight(float p_weight) {
		this.p_weight = p_weight;
	}

	public String getPlayer() {
		return player;
	}

	public void setPlayer(String player) {
		this.player = player;
	}

	public String getTeacher() {
		return teacher;
	}

	public void setTeacher(String teacher) {
		this.teacher = teacher;
	}

}
